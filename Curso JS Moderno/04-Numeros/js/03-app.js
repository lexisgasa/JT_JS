// el objeto math existe en la ventana global de js y tiene diferentes funciones como

// el numero pi
console.log(Math.PI)

// redondear abajo
console.log(Math.floor(2.7))


// redondear hacia arriba
console.log(Math.ceil(2.1))


// redondear hacia abajo o arriba depende del .X 
